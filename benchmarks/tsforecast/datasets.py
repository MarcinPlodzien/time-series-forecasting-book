"""
datasets.py
===========

Loading and splitting of the benchmark signals.

For now we ship a single signal, the Santa Fe laser (competition set A). The
loader returns the raw series plus the canonical train / continuation split used
throughout the benchmarks. Keeping the split in one place means every experiment
forecasts the same target, so the cross-method comparison is meaningful.

Why a dataclass rather than a bare array?
    A forecasting problem is the numbers plus a decision about where memory of
    the past ends and prediction begins. Bundling the series with its split makes
    that decision explicit and reusable, and keeps the forecasting object fixed
    before any model is chosen (Chapter 4).
"""

from __future__ import annotations

import os
from dataclasses import dataclass

import numpy as np

# Absolute path to the data directory, resolved relative to THIS file so the
# experiments run from any working directory.
_DATA_DIR = os.path.join(os.path.dirname(os.path.abspath(__file__)), "..", "..", "data", "benchmarks")


@dataclass
class TimeSeriesSplit:
    """A univariate series together with its train / test partition.

    Attributes
    ----------
    name : str
        Human-readable identifier, used in figure titles and result tables.
    series : np.ndarray
        The full 1-D record, float64, in its original physical units.
    train_len : int
        Number of leading samples that constitute the training set. Everything
        after this index is the held-out continuation we forecast against.
    """

    name: str
    series: np.ndarray
    train_len: int

    @property
    def train(self) -> np.ndarray:
        """The samples a model is allowed to learn from."""
        return self.series[: self.train_len]

    @property
    def test(self) -> np.ndarray:
        """The held-out continuation. A model never sees these during fitting."""
        return self.series[self.train_len :]

    def test_horizon(self, horizon: int) -> np.ndarray:
        """The first `horizon` continuation samples (the forecasting target)."""
        return self.series[self.train_len : self.train_len + horizon]


def load_santafe_laser(train_len: int = 1000) -> TimeSeriesSplit:
    """Load the Santa Fe laser series (competition set A).

    Parameters
    ----------
    train_len : int
        Length of the training set. The competition standard is 1000, which is
        what every experiment here uses; it is exposed only so a curious reader
        can shorten it and watch the methods degrade as data becomes scarce.

    Returns
    -------
    TimeSeriesSplit
    """
    path = os.path.join(_DATA_DIR, "santafe_laser_full.txt")
    # The file is one integer per line. loadtxt handles the whitespace padding.
    series = np.loadtxt(path, dtype=float)
    if series.ndim != 1:
        raise ValueError(f"expected a 1-D series, got shape {series.shape}")
    return TimeSeriesSplit(name="Santa Fe laser", series=series, train_len=train_len)


def load_mackey_glass(train_len: int = 1000) -> TimeSeriesSplit:
    """Mackey-Glass series, tau=17 (the standard chaotic regime).

    Synthetic and deterministic, generated by experiment 00 from its delay
    equation. Because it is generated, the true delay, attractor dimension
    (~2.1) and Lyapunov exponent are known, so the diagnostics can be checked
    against ground truth. Trains on the first 1000 samples by default, matching
    the laser so the two chaotic cases are comparable.
    """
    path = os.path.join(_DATA_DIR, "mackeyglass_tau17.txt")
    series = np.loadtxt(path, dtype=float)
    return TimeSeriesSplit(name="Mackey-Glass", series=series, train_len=train_len)


def load_enso(train_len: int = 600) -> TimeSeriesSplit:
    """ENSO Nino 3.4 monthly SST-anomaly index.

    A noisy quasi-periodic climate signal: a broad 2-7 year oscillation plus
    strong stochastic forcing, with a short predictability horizon. The series is
    already a seasonal anomaly, hence roughly stationary, so the fit-once
    rolling-origin protocol applies. Trains on the first 50 years (600 months).
    """
    path = os.path.join(_DATA_DIR, "enso_nino34.txt")
    series = np.loadtxt(path, dtype=float)
    return TimeSeriesSplit(name="ENSO (Nino 3.4)", series=series, train_len=train_len)


def load_sp500(
    kind: str = "logreturns", train_len: int = 2500, window: int = 5000
) -> TimeSeriesSplit:
    """S&P 500 as a forecasting object derived from the daily close.

    The raw file is the daily closing level (1927-2026). The level is a near
    random walk, so forecasting it just rewards persistence; the meaningful
    objects are derived from the log-returns r_t = log(p_t) - log(p_{t-1}):

        kind="logreturns" : the returns themselves. Near-stationary and
            near-uncorrelated, so the conditional mean is essentially
            unpredictable.
        kind="sqreturns"  : r_t^2, a proxy for variance. Returns are unpredictable
            in the mean but their magnitude is strongly autocorrelated (volatility
            clustering), so this object keeps forecastable structure.

    Keeps the most recent `window` returns (~5000 days, roughly 2006 on), the same
    window analysed in the Chapter 3 case study. Differencing to returns is also
    what makes the shared rolling-origin harness valid on this otherwise
    non-stationary signal.
    """
    path = os.path.join(_DATA_DIR, "sp500_daily_close.txt")
    if not os.path.exists(path):
        raise FileNotFoundError(
            f"{path} not found. The S&P 500 daily close ships with the repository; "
            "if it is missing, download it with `python data/fetch_sp500.py`."
        )
    price = np.loadtxt(path, dtype=float)
    logret = np.diff(np.log(price))
    if window is not None and window < len(logret):
        logret = logret[-window:]  # most recent `window` returns
    if kind == "logreturns":
        series, name = logret, "S&P 500 (log-returns)"
    elif kind == "sqreturns":
        series, name = logret ** 2, "S&P 500 (squared returns)"
    else:
        raise ValueError(f"unknown kind {kind!r}; use 'logreturns' or 'sqreturns'")
    return TimeSeriesSplit(name=name, series=series, train_len=train_len)
