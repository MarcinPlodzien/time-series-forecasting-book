"""
models/nvar.py
==============

Nonlinear Vector AutoRegression (NVAR), a.k.a. next-generation reservoir
computing (Gauthier et al. 2021): one of the matched models.

The recipe is fully deterministic:

    1. Build a delay vector from the last `k` samples (Takens embedding).
    2. Form an explicit nonlinear feature map of that vector: a constant, the
       linear terms, and all polynomial terms up to degree `degree` (here 2).
    3. Train a single linear (ridge) readout from features to the next sample.

This is the "nonlinear representation of the past, linear readout of the future"
template of Chapter 4 in its most explicit form, with the nonlinearity written
down by hand instead of generated by a random reservoir. Because the polynomial
features can represent the quadratic folding of the laser-Lorenz dynamics, the
model can curve the trajectory back and anticipate the collapse. This is the
matched behaviour, and the contrast with the linear AR model is the experiment.

Why NVAR rather than a large reservoir for the matched side? It is deterministic
(no random seed), has only three knobs (delay count, spacing, polynomial degree),
and is known to match or beat echo-state networks on this class of
low-dimensional chaotic systems, which makes it a clean matched case.
"""

from __future__ import annotations

import itertools

import numpy as np

from ..base import Forecaster, Standardiser
from ..readout import ridge_solve, select_ridge_closedloop


class NVAR(Forecaster):
    """Polynomial next-generation reservoir computer with a ridge readout."""

    def __init__(
        self,
        n_delays: int = 4,
        stride: int = 1,
        degree: int = 2,
        ridge: float | None = None,
    ) -> None:
        """
        n_delays : how many delayed samples enter the linear part (k).
        stride   : spacing between those delays (the embedding tau).
        degree   : maximum total degree of the polynomial features.
        ridge    : Tikhonov regularisation for the readout. None (default)
                   selects it on a validation split; a float fixes it.
        """
        self.n_delays = n_delays
        self.stride = stride
        self.degree = degree
        self.ridge = ridge
        self.name = f"NVAR (k={n_delays}, deg={degree})"
        self._scaler = Standardiser()
        self._W: np.ndarray | None = None  # readout weights, (n_features,)

    # ---- feature map -------------------------------------------------------
    def _features(self, lagvec: np.ndarray) -> np.ndarray:
        """Map a delay vector to [1, linear terms, all monomials up to degree].

        Example for a 2-D lag vector [a, b] and degree 2:
            [1, a, b, a^2, a*b, b^2].
        The constant term lets the readout encode an offset; the monomials are
        the nonlinear coordinates that make folding representable.
        """
        terms = [1.0]
        terms.extend(lagvec)  # degree-1 (linear) terms
        for d in range(2, self.degree + 1):
            # All combinations with repetition give the distinct monomials of
            # total degree d (e.g. a^2, a*b, b^2 for d=2).
            for combo in itertools.combinations_with_replacement(range(len(lagvec)), d):
                terms.append(np.prod([lagvec[i] for i in combo]))
        return np.array(terms)

    def _lag_indices(self) -> list[int]:
        """Offsets (into the past) of the delayed samples, newest first."""
        return [j * self.stride for j in range(self.n_delays)]

    # ---- training ----------------------------------------------------------
    def fit(self, train: np.ndarray) -> "NVAR":
        train = np.asarray(train, float)
        if self.ridge is None:  # pick ridge by closed-loop capacity on a val tail
            self._eff_ridge = select_ridge_closedloop(
                lambda r: NVAR(n_delays=self.n_delays, stride=self.stride,
                               degree=self.degree, ridge=r),
                train)
        else:
            self._eff_ridge = float(self.ridge)
        z = self._scaler.fit(train).transform(train)
        offsets = self._lag_indices()
        span = offsets[-1]
        # Build the feature matrix Phi and target y for every valid time index.
        feats, targets = [], []
        for t in range(span, len(z) - 1):
            lagvec = np.array([z[t - o] for o in offsets])  # newest..oldest
            feats.append(self._features(lagvec))
            targets.append(z[t + 1])
        Phi = np.array(feats)
        y = np.array(targets)
        # Ridge regression with the ridge chosen above.
        self._W = ridge_solve(Phi, y, self._eff_ridge)
        self._ridge_used = self._eff_ridge
        return self

    # ---- closed-loop rollout ----------------------------------------------
    def forecast(self, horizon: int, warmup: np.ndarray | None = None) -> np.ndarray:
        if self._W is None:
            raise RuntimeError("call fit first")
        offsets = self._lag_indices()
        span = offsets[-1]
        warmup = warmup if warmup is not None else np.zeros(span + 1)
        hist = list(self._scaler.transform(np.asarray(warmup, float)))
        preds = []
        for _ in range(horizon):
            lagvec = np.array([hist[-1 - o] for o in offsets])  # newest..oldest
            nxt = self._scaler.clamp(float(self._features(lagvec) @ self._W))
            preds.append(nxt)
            hist.append(nxt)  # feed (bounded) prediction back in (closed loop)
        return self._scaler.inverse(np.array(preds))
